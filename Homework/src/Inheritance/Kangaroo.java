package Inheritance;

public class Kangaroo extends Mammal{
	
	private int jumpHeight;
	private boolean hasAPouch;
	
	
	public int getJumpHeight() {
		return jumpHeight;
	}
	public void setJumpHeight(int jumpHeight) {
		this.jumpHeight = jumpHeight;
	}
	public boolean isHasAPouch() {
		return hasAPouch;
	}
	public void setHasAPouch(boolean hasAPouch) {
		this.hasAPouch = hasAPouch;
	}
}
